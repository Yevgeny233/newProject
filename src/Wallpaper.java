public class Wallpaper {
    int price;
    String color;

    public Wallpaper(int price, String color) {
        this.price = price;
        this.color = color;
    }

    public int getPrise() {
        return price;
    }

    public void setPrise(int prise) {
        this.price = prise;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

}
